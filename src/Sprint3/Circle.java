package Sprint3;

public class Circle {
        public static void main(String[] args) {
            double radius;
            double pi;
            double perimetr;
            double area;
            radius = 5.5;
            pi = 3.14159265;
            perimetr = 2 * radius * pi;
            area = radius * radius * pi;
            System.out.println("For circle of radius 5.5 area is " + area + "and perimeter is " + perimetr);
        }
}
